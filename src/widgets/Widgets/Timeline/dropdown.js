    static idNameToDisplayName(idName) {
        return Dropdown.displayNames[Dropdown.idNames.indexOf(idName)];
    }

    static fillDropDown(dropdownID, currentSelection) {
        let dropdown = document.getElementById(dropdownID);
        for (let id of Dropdown.idNames) {
            let option = document.createElement('option');
            option.setAttribute('value', id);
            option.innerHTML = Dropdown.idNameToDisplayName(id);
            dropdown.appendChild(option);
        }
        dropdown.value = currentSelection;
        return dropdown;
    }
